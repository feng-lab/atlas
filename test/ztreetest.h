/* treetest.swc is a forest contains three trees
//             1             11       15
//          /  |  \          |
//         2   3   4         12
//            / \   \       / \
//           5   6   7     13 14
//          / \  \
//         8  9  10
*/

TEST(SWC, IteratorInteraction)
{
  using namespace nim;

  try {
    ZSwc::PreOrderIterator proit;
    ZSwc::ConstPreOrderIterator cproit;
    ZSwc::ReversePreOrderIterator rproit;
    ZSwc::ConstReversePreOrderIterator crproit;
    ASSERT_EQ(cproit, proit);
    ASSERT_EQ(crproit, rproit);

    ZSwc::PostOrderIterator poit;
    ZSwc::ConstPostOrderIterator cpoit;
    ZSwc::ReversePostOrderIterator rpoit;
    ZSwc::ConstReversePostOrderIterator crpoit;
    ASSERT_EQ(poit, proit);
    ASSERT_EQ(cpoit, proit);
    ASSERT_EQ(rpoit, rproit);
    ASSERT_EQ(crpoit, rproit);

    ZSwc::BreadthFirstIterator bfit;
    ZSwc::ConstBreadthFirstIterator cbfit;
    ZSwc::ReverseBreadthFirstIterator rbfit;
    ZSwc::ConstReverseBreadthFirstIterator crbfit;
    ASSERT_EQ(bfit, proit);
    ASSERT_EQ(cbfit, proit);
    ASSERT_EQ(rbfit, rproit);
    ASSERT_EQ(crbfit, rproit);

    ZSwc::ChildIterator cit;
    ZSwc::ConstChildIterator ccit;
    ZSwc::ReverseChildIterator rcit;
    ZSwc::ConstReverseChildIterator crcit;
    ASSERT_EQ(cit, proit);
    ASSERT_EQ(ccit, proit);
    ASSERT_EQ(rcit, rproit);
    ASSERT_EQ(crcit, rproit);

    ZSwc::AncestorIterator ait;
    ZSwc::ConstAncestorIterator cait;
    ZSwc::ReverseAncestorIterator rait;
    ZSwc::ConstReverseAncestorIterator crait;
    ASSERT_EQ(ait, proit);
    ASSERT_EQ(cait, proit);
    ASSERT_EQ(rait, rproit);
    ASSERT_EQ(crait, rproit);

    ZSwc::LeafIterator lit;
    ZSwc::ConstLeafIterator clit;
    ZSwc::ReverseLeafIterator rlit;
    ZSwc::ConstReverseLeafIterator crlit;
    ASSERT_EQ(lit, proit);
    ASSERT_EQ(clit, proit);
    ASSERT_EQ(rlit, rproit);
    ASSERT_EQ(crlit, rproit);

    ZSwc::Iterator pit;
    ZSwc::ConstIterator cpit;
    ZSwc::ReverseIterator rpit;
    ZSwc::ConstReverseIterator crpit;
    ASSERT_EQ(pit, proit);
    ASSERT_EQ(cpit, proit);
    ASSERT_EQ(rpit, rproit);
    ASSERT_EQ(crpit, rproit);

    ZSwc::RootIterator rit;
    ZSwc::ConstRootIterator crit;
    ZSwc::ReverseRootIterator rrit;
    ZSwc::ConstReverseRootIterator crrit;
    ASSERT_EQ(rit, proit);
    ASSERT_EQ(crit, proit);
    ASSERT_EQ(rrit, rproit);
    ASSERT_EQ(crrit, rproit);

    ZSwc swc(getTestDataDir().filePath("treetest.swc"));

    // not necessary but good to have
    ASSERT_EQ(swc.cend(), swc.end());
    ASSERT_EQ(swc.cendPostOrder(), swc.end());
    ASSERT_EQ(swc.endPostOrder(), swc.end());
    ASSERT_EQ(swc.cendBreadthFirst(), swc.end());
    ASSERT_EQ(swc.endBreadthFirst(), swc.end());
    ASSERT_EQ(swc.cendLeaf(), swc.end());
    ASSERT_EQ(swc.endLeaf(), swc.end());
    ASSERT_EQ(swc.cendRoot(), swc.end());
    ASSERT_EQ(swc.endRoot(), swc.end());

    auto sit = swc.begin();
    ASSERT_EQ(swc.cend(sit), swc.end(sit));
    ASSERT_EQ(swc.cendPostOrder(sit), swc.end(sit));
    ASSERT_EQ(swc.endPostOrder(sit), swc.end(sit));
    ASSERT_EQ(swc.cendBreadthFirst(sit), swc.end(sit));
    ASSERT_EQ(swc.endBreadthFirst(sit), swc.end(sit));
    ASSERT_EQ(swc.cendLeaf(sit), swc.end(sit));
    ASSERT_EQ(swc.endLeaf(sit), swc.end(sit));
    ASSERT_EQ(swc.cendChild(sit), swc.end(sit));
    ASSERT_EQ(swc.endChild(sit), swc.end(sit));
    ASSERT_EQ(swc.cendAncestor(sit), swc.end(sit));
    ASSERT_EQ(swc.endAncestor(sit), swc.end(sit));

    // it is possible to convert type of iterator
    const int preRes[] = {1, 2, 3, 5, 8, 9, 6, 10, 4, 7, 11, 12, 13, 14, 15};
    const int postRes[] = {2, 8, 9, 5, 10, 6, 3, 7, 4, 1, 13, 14, 12, 11, 15};
    const int bfRes[] = {1, 11, 15, 2, 3, 4, 12, 5, 6, 7, 13, 14, 8, 9, 10};
    const int rootRes[] = {1, 11, 15};
    size_t sidx = 0;
    cpit = swc.cbegin();
    for (; cpit != swc.cend() && cpit->id != 6; ++cpit) {
      ASSERT_EQ(cpit->id, preRes[sidx++]);
    }
    ASSERT_EQ(cpit->id, 6);
    // convert to post iterator
    cpoit = cpit;
    sidx = 5;
    for (; cpoit != swc.cendPostOrder() && cpoit->id != 1; ++cpoit) {
      ASSERT_EQ(cpoit->id, postRes[sidx++]);
    }
    ASSERT_EQ(cpoit->id, 1);
    // convert to breadth first iterator
    cbfit = cpoit;
    sidx = 0;
    for (; cbfit != swc.cendBreadthFirst() && cbfit->id != 8; ++cbfit) {
      ASSERT_EQ(cbfit->id, bfRes[sidx++]);
    }
    ASSERT_EQ(cbfit->id, 8);
    // convert to pre order iterator
    cpit = cbfit;
    sidx = 4;
    for (; cpit != swc.cend() && cpit->id != 11; ++cpit) {
      ASSERT_EQ(cpit->id, preRes[sidx++]);
    }
    ASSERT_EQ(cpit->id, 11);
    // convert to root iterator
    crit = cpit;
    sidx = 1;
    for (; crit != swc.cendRoot() && crit->id != 15; ++crit) {
      ASSERT_EQ(crit->id, rootRes[sidx++]);
    }
    ASSERT_EQ(crit->id, 15);
    // convert to breadth first iterator and finish
    cbfit = crit;
    sidx = 2;
    for (; cbfit != swc.cendBreadthFirst(); ++cbfit) {
      ASSERT_EQ(cbfit->id, bfRes[sidx++]);
    }
    ASSERT_EQ((--cbfit)->id, 10);

    // do same stuff reversely, it is not as straight forward as the forward version as reverse_iterator
    // stores next position (not current position)
    sidx = std::extent<decltype(preRes)>::value;
    crpit = swc.crbegin();
    for (; crpit != swc.crend() && crpit->id != 8; ++crpit) {
      ASSERT_EQ(crpit->id, preRes[--sidx]);
    }
    ASSERT_EQ(crpit->id, 8);
    // convert to breadth first reverse iterator
    auto tmp = crpit.base();
    crbfit = std::make_reverse_iterator(--tmp);
    --crbfit;
    sidx = std::extent<decltype(bfRes)>::value - 2;
    for (; crbfit != swc.crendBreadthFirst() && crbfit->id != 15; ++crbfit) {
      ASSERT_EQ(crbfit->id, bfRes[--sidx]);
    }
    ASSERT_EQ(crbfit->id, 15);
    // convert to root reverse iterator
    auto tmp1 = crbfit.base();
    crrit = std::make_reverse_iterator(--tmp1);
    --crrit;
    sidx = std::extent<decltype(rootRes)>::value;
    for (; crrit != swc.crendRoot() && crrit->id != 1; ++crrit) {
      ASSERT_EQ(crrit->id, rootRes[--sidx]);
    }
    ASSERT_EQ(crrit->id, 1);
    // convert to post order reverse iterator and finish
    auto tmp2 = crrit.base();
    crpoit = std::make_reverse_iterator(--tmp2);
    --crpoit;
    sidx = std::extent<decltype(postRes)>::value - 5;
    for (; crpoit != swc.crendPostOrder(); ++crpoit) {
      ASSERT_EQ(crpoit->id, postRes[--sidx]);
    }
    ASSERT_EQ((--crpoit)->id, 2);
  }
  catch (const ZIOException & e) {
    LOG(WARNING) << e.what();
  }
}
